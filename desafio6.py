# 2º Desafio crie um algoritmo que leia um numero e mostre o seu dobro, triplo e raiz quadrada

numero = int(input('Digite um número: '))
print('Seu numero é: {}'.format(numero))
print('O dobro dele é: {}'.format(numero*2))
print('O triplo dele é: {}'.format(numero*3))
print('A raiz quadrada de {25}'.format(numero**(1/2)))